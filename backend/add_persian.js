const mongoose = require('mongoose');
require('dotenv').config({ path: path.join(__dirname, '.env') });
const Word = require('./models/Word');

async function run() {
    await mongoose.connect(process.env.MONGODB_URI);
    console.log('Connected to DB');
    
    // We update Word model in DB
    const words = await Word.find({});
    for (let w of words) {
        if (PERSIAN_MAP[w.word]) {
            w.persian = PERSIAN_MAP[w.word];
            await w.save();
        } else {
            console.log("Missing mapping for", w.word);
        }
    }
    
    // Also add remaining ones if any
    for (let [word, persian] of Object.entries(PERSIAN_MAP)) {
        let existing = await Word.findOne({ word });
        if (!existing) {
            await Word.create({ word, meaning: '...', persian });
        }
    }
    console.log('Updated Persian words in DB!');
    process.exit(0);
}
run();
